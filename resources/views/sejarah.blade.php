    <h1>Sejarah Gereja Paroki St.Yoseph Parsoburan</h1><br>
    <text>
        Paroki St.Yoseph Parsoburan merupakan salah satu paroki yang berada di wilayah Keuskupan Agung Medan.
        Paroki yang saat ini dipimpin oleh RD.Henri Simbolon, beralamat di Kel.Parsoburan Tengah, Rt IV, Kec.Habinsaran, Kab Toba.
        Wilayahnya tersebar di 3 kecamatan yakni, Kec.Habinsaran, Kec.Nassau dan Kec.Borbor.
        Paroki ini terdiri dari 26 stasi yang terbagi dalam 6 Rayon dan 10 lingkungan di paroki.
    </text><br><br>
    <h3>Menyemaikan Iman di Paroki Parsoburan</h3><br>
    <text>
        Gereja Katolik berdiridi Parsoburan tidak lepas dari hubungan kekerabatan antara Parsoburan dengan desa Lumban Pinasa.
        Awalnya, Raja Gompul Siagian berniat menjadi Katolik, agar mereka bisa keluar dari belenggu Kepercayaan Animisme(Sipele begu).
        Raja Gompul Siagian bersama teman-temannya berangkat ke Balige menemui misionaris Belanda. Pada waktu itu, pusat misi di Balige.
        Mereka sangat berharap bahwa keluarga mereka, paling tidak keluarga dan teman sejalan ke Balige segera disahkan menjadi Katolik.
        Dengan segala perjuangan para umat perdana di Parsoburan, akhirnya misionaris berencana datang dan tinggal di Parsoburan. 
    </text><br><br>
    <text>
        Pada tahun 1952, parsoburan disahkan oleh keuskupan menjadi paroki (pemekaran dari Paroki Balige).
        Satu tahun setelah disahkan menjadi paroki, para misionaris tinggal di Parsoburan.
        Pada waktu yang sama, umat berencana mendirikan gereja yang baru.
        Keinginan ini terjawab setelah kehadiran RP.Rochus Raessens, OFM Cap diparsoburan pada tahun 1953.
        Sejak tahun 1959-1963, RP.Asterius Van Reen, OFm Cap tinggal menetap di Parsoburan.
        Setelah bermisi selama empat tahun, beliau cuti ke Belanda.
        Pada tahun 1965, RP.Nepomucenus Hamers, OFM Cap tiba di Parsoburan.
        Setelahnya suster Kongregasi Suster Santo Yosef(KSSY) resmi membuka rumah.
        Telah ada puluhan pastor kapusin yang membantu pelayanan di paroki ini. 
    </text><br><br>
    <text>
        Estafet Penggembalaan Beralih ke Imam Diosesan.
        Pada bulan Februari 2010, paroki Parsoburan mendapat rahmat baru,
        Paroki tersebut akan digembalakan oleh imam Diosesan yakni; RD.Anggiat Sihotang dan RD.Wirman Matondang.
        Mereka tinggal di pastoran yang sudah lama menjadi tempat tinggal para pastor.
        Berselang beberapa bulan, tepatnya pada Rabu, 15 September 2010, pastoran dan kantor paroki terbakar.
        Dua hari kemudian pada Jumat, 17 September 2010 malam, Dewan Paroki Parsoburan mengadakan rapat mendadak dan membentuk panitia pembangunan pastoran yang baru.
        Maka sejak tahun 2010, pastor paroki bersinergi dengan Dewan Paroki mencanangkann triple pembangunan; membangun pastoran, aula paroki, dan kantor paroki.
    </text><br><br>
    <text>
        Dengan segala usaha, para pengurus gereja menggalakkan gotong-royong dan pengumpulan dana untuk triple pembangunan.
        Akhirnya, pastoran, aula paroki, dan kantor paroki rampung pada tahun 2015.
        Rampungnya pembangunan ketiga item tersebut membuahkan semangat baru membangun beberapa stasti yakni:
        Stasi Sijungkat, Napajoring, Nassau, Borbor, Pararungan, LumbanRuhap, Sipange, Pandumaan, Paronggangan, Sitarak, Aek Ulok, Sibuntuon, Ambatan Lobu Hole, tornaganjang, Laverna, Lumban Lintong, Gereja Paroki, Sijomba, dan Lumban Rau.
    </text><br><br>
    
